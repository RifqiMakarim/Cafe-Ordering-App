    public void getTax( int t ){
        if(t >= 75000 && t <= 100000){
            tax = 1000;
        } else if( t > 100000 && t <= 250000 ) {
            tax = 1500;
        } else if( t > 250000 && t <= 500000 ) {
            tax = 2000;
        } else if( t > 500000 && t <= 1000000 ) {
            tax = 3000;
        } else if ( t > 1000000 ) {
            tax = 5000;
        }
    }
    
    
